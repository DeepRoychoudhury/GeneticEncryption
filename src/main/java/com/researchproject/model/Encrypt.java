//Pojo Class for Encryption
package com.researchproject.model;

public class Encrypt {

	private int id;
	private int fileid;
	private String data;
	private String accessibilityGroup;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getFileid() {
		return fileid;
	}
	public void setFileid(int fileid) {
		this.fileid = fileid;
	}
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	public String getAccessibilityGroup() {
		return accessibilityGroup;
	}
	public void setAccessibilityGroup(String accessibilityGroup) {
		this.accessibilityGroup = accessibilityGroup;
	}
	
	
}
